
-- Collection 
    -- It is a group of elements of the same datatype
    -- It generally comprises arrays,lists,sets and so on
    -- It is a single dimensional array
    -- It can be accessible through the index value
    -- Data's from the collection can be retrived using collections methods
    
    
    -- Dense Collection     :   Data's Fully Occupied
    -- Sparse Collection    :   Data's not Occupied Fully there are some gaps
    
    -- Dense Collection:
        -- 'A','B','C','D'
        
    -- Sparse Collection :
        -- 'A','B',' ','D',' '
        
        
    -- Types of Collections
        -- Index By Table (Associative Array)
        -- Nested Table
        -- Varray